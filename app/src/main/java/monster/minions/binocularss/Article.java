package monster.minions.binocularss;

public class Article {
    String title;
    String date;
    String source;
    String author;
    String text;
    String description;

    public Article(String title, String date, String source, String author, String text, String description) {
        this.title = title;
        this.date = date;
        this.source = source;
        this.author = author;
        this.text = text;
        this.description = description;
    }
}
